#!/usr/bin/env node
/**
 * migrate-articles.mjs
 *
 * Reads existing articles.js and peptides.js from taura-research/new_page/src/data/
 * and generates SQL INSERT statements for the Supabase `articles` table.
 *
 * Usage: node scripts/migrate-articles.mjs > supabase-migrations/002_seed_articles.sql
 * Or:    node scripts/migrate-articles.mjs --api  (sends directly to /api/articles)
 */

import { readFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

// Parse articles.js (ES module with export)
const articlesPath = resolve(root, "taura-research/new_page/src/data/articles.js");
const peptidesPath = resolve(root, "taura-research/new_page/src/data/peptides.js");

// We need to extract the array from `export const articles = [...]`
function parseJsExport(filePath, varName) {
  const content = readFileSync(filePath, "utf-8");
  // Remove export and eval as a module
  const fn = new Function(`
    ${content.replace(/^export\s+/gm, "")}
    return ${varName};
  `);
  return fn();
}

function escapeSQL(str) {
  if (str === null || str === undefined) return "NULL";
  return "'" + String(str).replace(/'/g, "''") + "'";
}

function escapeArray(arr) {
  if (!arr || arr.length === 0) return "'{}'";
  return "ARRAY[" + arr.map(s => escapeSQL(typeof s === "object" ? JSON.stringify(s) : s)).join(",") + "]::text[]";
}

const mode = process.argv[2];

try {
  const articles = parseJsExport(articlesPath, "articles");
  console.error(`Found ${articles.length} articles`);

  if (mode === "--api") {
    // Send directly to API
    const baseUrl = process.env.API_URL || "http://127.0.0.1:19001";
    for (const a of articles) {
      const payload = {
        slug: a.slug,
        title: a.title,
        category: a.tag || "Artigo",
        status: "published",
        body_html: a.bodyHtml || "",
        meta_description: a.title,
        tags: a.tag ? a.tag.split(" · ") : [],
        author: "TAURA Research",
      };
      try {
        const res = await fetch(`${baseUrl}/api/articles`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(payload),
        });
        const status = res.status;
        console.error(`  ${status === 201 ? "✅" : "❌"} ${a.slug} (${status})`);
      } catch (e) {
        console.error(`  ❌ ${a.slug} — ${e.message}`);
      }
    }
    console.error("Done!");
  } else {
    // Generate SQL
    console.log("-- Migration: Seed articles from existing articles.js");
    console.log("-- Generated by scripts/migrate-articles.mjs");
    console.log(`-- ${new Date().toISOString()}`);
    console.log("");
    console.log("INSERT INTO articles (slug, title, category, status, body_html, meta_description, tags, author, sources, related) VALUES");

    const values = articles.map((a, i) => {
      const slug = escapeSQL(a.slug || a.id);
      const title = escapeSQL(a.title);
      const category = escapeSQL(a.tag || "Artigo");
      const status = "'published'";
      const bodyHtml = escapeSQL(a.bodyHtml || "");
      const metaDesc = escapeSQL(a.title);
      const tags = a.tag ? escapeArray(a.tag.split(" · ")) : "'{}'";
      const author = "'TAURA Research'";
      const sources = a.sources ? escapeArray(a.sources) : "'{}'";
      const related = a.related ? escapeSQL(JSON.stringify(a.related)) : "NULL";

      return `(${slug}, ${title}, ${category}, ${status}, ${bodyHtml}, ${metaDesc}, ${tags}, ${author}, ${sources}, ${related})`;
    });

    console.log(values.join(",\n"));
    console.log("ON CONFLICT (slug) DO UPDATE SET");
    console.log("  title = EXCLUDED.title,");
    console.log("  category = EXCLUDED.category,");
    console.log("  body_html = EXCLUDED.body_html,");
    console.log("  meta_description = EXCLUDED.meta_description,");
    console.log("  tags = EXCLUDED.tags,");
    console.log("  sources = EXCLUDED.sources,");
    console.log("  related = EXCLUDED.related,");
    console.log("  updated_at = now();");
  }
} catch (e) {
  console.error("Error:", e.message);
  process.exit(1);
}
